<script type="text/javascript">
    $(document).ready(function () {
      var validator = $("#contact-form").validate({
        rules: {
          firstName: {required: true, Alphabets: true, maxlength: 50, minlength: 1},
          lastName: {required: true, Alphabets: true, maxlength: 50, minlength: 1},
          address: {required: true, maxlength: 50, minlength: 3},
          email: {
            required: true,
            email: true
          },
          phonenum: {required: true,
            phoneUS: true,
            maxlength: 12
          },
          secondaryphone: {required: false,
            phoneUS: true,
            maxlength: 12
          }
        },
        messages: {
          firstName: {required: "Please enter first name.", maxlength: "Please enter below 50 characters", minlength: "Please enter minimum 1 characters"},
          lastName: {required: "Please enter last name.", maxlength: "Please enter below 50 characters", minlength: "Please enter minimum 1 characters"},
          address: {required: "Please enter address.", maxlength: "Please enter below 50 characters", minlength: "Please enter minimum 3 characters"},
          email: {required: "Please enter email.", email: "Please enter valid email"},
          phonenum: {required: "Please enter phone number.", maxlength: "Please enter 10 numbers only"},
        },
        submitHandler: function (form) {
          $.ajax({
            url: "contactmail.php",
            type: "POST",
            data: $('#contact-form').serialize(),
            async: false,
            success: function (result)
            {
              if (result == 1) {
                $('#mailfail').hide();
                $('#mailsuccess').show();
                $("#contact-form")[0].reset();
              } else {
                $('#mailsuccess').hide();
                $('#mailfail').show();
              }
            }
          });
        }
      });
      $("button.reset").click(function() {
        validator.resetForm();
        $("#contact-form")[0].reset();
      });
    });
    $.validator.addMethod("Alphabets", function (value, element) {
          return this.optional(element) || /^[a-zA-Z\s]+$/.test(value);
          // just ascii letters
        }, "Name should contain only alphabets");
    $.validator.addMethod("phoneUS", function (phone_number, element) {
        phone_number = phone_number.replace(/\s+/g, "");
        return this.optional(element) || phone_number.length > 9 &&
            phone_number.match(/^(\+?1-?)?(\([2-9]([02-9]\d|1[02-9])\)|[2-9]([02-9]\d|1[02-9]))-?[2-9]([02-9]\d|1[02-9])-?\d{4}$/);
      }, "Please enter valid US phone number");

  </script>
  
